package geometria;

public class Aree {
	

	
	public static int triangolo(int a, int b) {
		
		return (a*b)/2;
		
	}
	
	public static int quadrato(int a) {
		
		return a*a;
		
	}
	
	public static int rombo(int a, int b) {
		
		int A;
		
		A = a*b/2;
		
		return A;
		
		
		
	}
	
	public static int trapezio(int a, int b, int c) {
		
		return ((a+b)*c)/2;
	}

	

}
